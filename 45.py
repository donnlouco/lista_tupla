# 45. Faca uma funcao que receba uma lista de listas e retorne uma lista ”achatada”(flatten).


import numpy as np
lista_aninhada = [[1, 2], [3, 4], [5]]
array_numpy = np.array(lista_aninhada)
array_plano = array_numpy.flatten()
print(array_plano)  # Saída: [1 2 3 4 5]


